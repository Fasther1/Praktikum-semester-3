package pertemuan7;

public class BilanganGanjilGenap {
    public static void main(String[] args) {
        
        System.out.println("genap:");
        int i = 0;
        while (i <= 20) {
            System.out.print(i + " ");
            i += 2;
        }

    
        System.out.println("\nganjil:");
        i = 1;
        while (i <= 20) {
            System.out.print(i + " ");
            i += 2;
        }
    }
}
