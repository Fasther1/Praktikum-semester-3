class KonversiSuhu {
    // Method konversi dari Celsius ke Fahrenheit
    public double celciusToFahrenheit(double celsius) {
        return (celsius * 9 / 5) + 32;
    }
    
    // Method konversi dari Celsius ke Reamur
    public double celciusToReamur(double celsius) {
        return celsius * 4 / 5;
    }
}